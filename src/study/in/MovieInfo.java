    public MovieInfo(String mname) {
        initComponents();
        mtitle.setText(mname);
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cinerateruser", "root", "1234");
            String sql = "select * from movie where title = '" + mname + "'";
            PreparedStatement stmt = con.prepareStatement(sql);
            ResultSet rs = stmt.executeQuery();
            if (rs.next()) {
                rdate.setText(rs.getString("release_date"));
                lang.setText(rs.getString("language"));
                gen.setText(rs.getString("genre"));
                movId = rs.getInt("id");
                int id = rs.getInt("id");
                sql = "select * from actedIn where movieId = " + id;
                stmt = con.prepareStatement(sql);
                String actors = "";
                rs = stmt.executeQuery();
                while(rs.next()){
                    sql = "select * from actor where id = " + rs.getInt("actId");
                    stmt = con.prepareStatement(sql);
                    ResultSet ab = stmt.executeQuery();
                    while(ab.next()){
                        actors = actors + ", " + ab.getString("name");
                    }
                }
                actor.setText(actors);
            }
            
            stmt.close();
            con.close();
        }
        catch(HeadlessException | ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(null, "no connection"+e);
        }
    }
