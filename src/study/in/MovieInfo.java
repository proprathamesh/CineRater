    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        String rate = rating.getText();
        String username = user.getText();
        String password = pass.getText();
        try{
            int intValue = Integer.parseInt(rate);
            if(intValue > 5){
                JOptionPane.showMessageDialog(null, "Please enter value less than 5");
            }
            else if(username == "" || password == ""){
                JOptionPane.showMessageDialog(null, "Please enter login credentials");
            }
            else{
                try{
                    Class.forName("com.mysql.cj.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cinerateruser", "root", "1234");
                    String sql = "select * from log_dets where username = '" + username + "' and password = '" + password + "'";
                    PreparedStatement stmt = con.prepareStatement(sql);
                    ResultSet rs = stmt.executeQuery();
                    if (rs.next()) {
                        sql = "Insert into rating values('" + username + "', '" + movId + "', '" + rate + "');";
                        stmt = con.prepareStatement(sql);
                        int rowsAffected = stmt.executeUpdate();

                        if (rowsAffected > 0) {
                            JOptionPane.showMessageDialog(null, "Rating added " + movId);
                        } else {
                            JOptionPane.showMessageDialog(null, "Rating not added ");
                        }
                    }
                    stmt.close();
                    con.close();
                }
                catch(HeadlessException | ClassNotFoundException | SQLException e){
                    JOptionPane.showMessageDialog(null, "no connection"+e);
                }
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Entered value is not an integer.");
        }
    }//GEN-LAST:event_saveActionPerformed

